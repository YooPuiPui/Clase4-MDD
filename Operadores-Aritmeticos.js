//! Dado los siguiente valores numéricos:
//!   const numero1 = 20;
//!   const numero2 = 40;


const numero1 = 20;
const numero2 = 40;

console.log("Numero 1 = 20");
console.log("Numero 2 = 40");


//? Suma

const suma = numero1 + numero2;
console.log("El resultado de la suma es: ", suma);


//? Resta

const resta = numero1 - numero2;
console.log("El resultado de la resta es: ", resta);


//? Multiplicacion

const mul= numero1 * numero2;
console.log("El resultado de la multiplicacion es: ", mul);


//? Modulo

const modulo = numero1 % numero2;
console.log("El modulo entre N°1 y N°2 es: ", modulo);



